let testCount = 0;
let passed = 0;
let failed = 0;

exports.nextTest = function(testDescription) {

    console.log()
    console.log("=====================================================================================================")
    console.log(testDescription)
    console.log("=====================================================================================================")
    console.log()
}

exports.nextTestUsingCounter = function(testDescription) {

    console.log()
    console.log("=====================================================================================================")
    console.log("TEST "+ (++testCount) + " - " + testDescription)
    console.log("=====================================================================================================")
    console.log()
}

exports.nextTestUsingCounterAssertTrue = function(testDescription, condition) {

    console.log()
    console.log("=====================================================================================================")
    console.log("TEST "+ (++testCount) + " - " + testDescription)
    console.log("=====================================================================================================")
    console.log()
    if(condition) {
        console.log("=====================================================================================================")
        console.log("TEST: \tPASSED ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++")
        console.log("=====================================================================================================")
        passed++;
    } else {
        console.log("=====================================================================================================")
        console.log("TEST: \tFAILED ----------------------------------------------------------------------------------")
        console.log("=====================================================================================================")
        failed++;
    }
}

exports.assertConditionTrue = function(condition) {
    if(condition) {
        console.log("=====================================================================================================")
        console.log("TEST: \tPASSED +")
        console.log("=====================================================================================================")
        passed++;
    } else {
        console.log("=====================================================================================================")
        console.log("TEST: \tFAILED -")
        console.log("=====================================================================================================")
        failed++;
    }
}

exports.printResults = function() {

    console.log()
    console.log("#####################################################################################################")
    console.log("TEST SUMMARY")
    console.log("==============================================")
    console.log("TOTAL TESTS PASSED:\t" + passed)
    console.log("TOTAL TESTS FAILED:\t" + failed)
    console.log()
    console.log("#####################################################################################################")
    console.log()
}